package tictactoe;

/**
 *
 * @author Umair
 */
public class Player {
    private String name;
    private String symbol;

    public Player() {
    }

    public Player(String name, String symbol) {
        this.name = name;
        this.symbol = symbol;
    }

    public String getName() {
        return name;
    }

    public String getSymbol() {
        return symbol;
    }
    
    
}
